static void prvHeapInit( void)
{
BlockLink_t *pxFirstFreeBlock;
uint8_t *pucAlignedHeap;

	MaxHeapSize[Kernel_Heap] = configADJUSTED_HEAP_SIZE;

	/* Ensure the heap starts on a correctly aligned boundary. */
	pucAlignedHeap = ( uint8_t * ) ( ( ( portPOINTER_SIZE_TYPE ) &ucHeap[ portBYTE_ALIGNMENT ] ) & ( ~( ( portPOINTER_SIZE_TYPE ) portBYTE_ALIGNMENT_MASK ) ) );

	/* xStart is used to hold a pointer to the first item in the list of free
	blocks.  The void cast is used to prevent compiler warnings. */
	xStart[CurHeapIdx].pxNextFreeBlock = ( void * ) pucAlignedHeap;
	xStart[CurHeapIdx].xBlockSize = ( size_t ) 0;

	/* xEnd is used to mark the end of the list of free blocks. */
	xEnd[CurHeapIdx].xBlockSize = MaxHeapSize[CurHeapIdx];
	xEnd[CurHeapIdx].pxNextFreeBlock = NULL;

	/* To start with there is a single free block that is sized to take up the
	entire heap space. */
	pxFirstFreeBlock = ( void * ) pucAlignedHeap;
	pxFirstFreeBlock->xBlockSize = MaxHeapSize[CurHeapIdx];
	pxFirstFreeBlock->pxNextFreeBlock = &xEnd[CurHeapIdx];
}
